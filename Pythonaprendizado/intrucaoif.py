#if é uma forma de avaliar e interpretar uma condição. Se for verdadeira, o bloco do if é executado.

print('Exemplo de if - se nota for maior que 7, mostra mensagem de aprovado, senão reprovado.')

nota = float(input('Digite a nota: '))
if nota >= 7:
    print('Aprovado!')
else:
    print('Reprovado!')
