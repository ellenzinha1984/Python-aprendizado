#Dicionários - informações com chave e valor, delimitado por {}

dicionario = {'nome':'Bela', 'espécie':'canina', 'idade':10, 'cor':'caramelo', 'raça':'SRD'}
print('Imprimindo o conteúdo do dicionário:')
print(dicionario)
print('Imprimindo o conteúdo de nome:')
print(dicionario['nome'])
print('Imprimindo nome, espécie e raça:')
print(dicionario['nome'],dicionario['espécie'], dicionario['raça'])